    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
        
        String tblphieuid = tblModel.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String tblsachid = tblModel.getValueAt(jTable1.getSelectedRow(), 1).toString();
        String tblname = tblModel.getValueAt(jTable1.getSelectedRow(), 2).toString();
        String tblngaymuon = tblModel.getValueAt(jTable1.getSelectedRow(), 3).toString();
        String tblngaytra = tblModel.getValueAt(jTable1.getSelectedRow(), 4).toString();
        
        txtphieuid.setText(tblphieuid);
        txtsachid.setText(tblsachid);
        txtname.setText(tblname);
        txtngaymuon.setText(tblngaymuon);
        txtngaytra.setText(tblngaytra);

    }//GEN-LAST:event_jTable1MouseClicked

