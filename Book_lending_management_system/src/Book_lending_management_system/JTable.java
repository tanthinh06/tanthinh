    private void ButtonsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonsuaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRowCount()==1){
            String phieuid = txtphieuid.getText();
            String sachid = txtsachid.getText();
            String name = txtname.getText();
            String ngaymuon = txtngaymuon.getText();
            String ngaytra = txtngaytra.getText();
            
            tblModel.setValueAt(phieuid, jTable1.getSelectedRow(), 0);
            tblModel.setValueAt(sachid, jTable1.getSelectedRow(), 1);
            tblModel.setValueAt(name, jTable1.getSelectedRow(), 2);
            tblModel.setValueAt(ngaymuon, jTable1.getSelectedRow(), 3);
            tblModel.setValueAt(ngaytra, jTable1.getSelectedRow(), 4);
            
            JOptionPane.showMessageDialog(this, "Update successfully..");
        }else{
            if(jTable1.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Table is empty..");
            }else{
                JOptionPane.showMessageDialog(this, "Pleasr select single Row for Update. . ");
            }
        }   
    }//GEN-LAST:event_ButtonsuaActionPerformed

    private void ButtionthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtionthemActionPerformed
        // TODO add your handling code here:
        if (txtphieuid.equals("") || txtsachid.equals("") || txtname.equals("") || txtngaymuon.equals("") || txtngaytra.equals("")) {
            JOptionPane.showMessageDialog(this, "Please Enter All Data !");
        } else {
            String data[] = {txtphieuid.getText(), txtsachid.getText(), txtname.getText(), txtngaymuon.getText(), txtngaytra.getText()};

            DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
            tblModel.addRow(data);
            JOptionPane.showMessageDialog(this, " All Data successfully..! !");

            txtphieuid.setText("");
            txtsachid.setText("");
            txtname.setText("");
            txtngaymuon.setText("");
            txtngaytra.setText("");
        }
    }//GEN-LAST:event_ButtionthemActionPerformed

    private void ButtionxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtionxoaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRowCount() == 1) {
            tblModel.removeRow(jTable1.getSelectedRow());
        } else {
            if (jTable1.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Table is empty.");
            } else {
                JOptionPane.showMessageDialog(this, "please select single row for Delete. ");

            }
        }
    }//GEN-LAST:event_ButtionxoaActionPerformed

